def 게임시작():  # 게임을 시작하는 함수를 정의

    숫자 = 1  # 게임에서 사용할 숫자 변수를 1로 초기화
    플레이어차례 = False  # 플레이어의 차례 여부를 False로 초기화

    while True:  # 무한 반복

        if 플레이어차례:  # 플레이어의 차례일 때

            사용자입력 = input()  # 사용자로부터 입력을 받습니다

            # 사용자가 입력한 값이 숫자이고, 입력한 숫자가 순서대로이며, 숫자에 3, 6, 9가 포함되지 않는 경우
            if (사용자입력.isdigit() and int(사용자입력) == 숫자 and
                    str(숫자).count('3') + str(숫자).count('6') + str(숫자).count('9') == 0):

                숫자 += 1  # 다음 숫자로 이동
                플레이어차례 = False  # 컴퓨터의 차례로 변경

            # 사용자가 입력한 값이 "짝", "짝짝", "짝짝짝" 중 하나인 경우
            elif 사용자입력 in ["짝", "짝짝", "짝짝짝"]:
                카운트 = str(숫자).count('3') + str(숫자).count('6') + str(숫자).count('9')
                # 숫자에 포함된 3, 6, 9의 개수를 카운트

                # 카운트가 0보다 크고, 카운트가 사용자 입력의 길이와 동일한 경우
                if 카운트 > 0 and 카운트 == len(사용자입력):
                    숫자 += 1  # 다음 숫자로 이동
                    플레이어차례 = False  # 컴퓨터의 차례로 변경

            else:
                print("틀렸습니다. 다시 시도하세요.")  # 조건에 맞지 않는 입력인 경우 오류 메시지를 출력

        else:  # 컴퓨터의 차례일 때

            # 숫자에 3, 6, 9가 없는 경우에는 숫자를 그대로 사용하고, 있을 경우 "짝"을 해당 개수만큼 반복하여 사용
            컴퓨터입력 = str(숫자) if str(숫자).count('3') + str(숫자).count('6') + str(숫자).count('9') == 0 else "짝" * (str(숫자).count('3') + str(숫자).count('6') + str(숫자).count('9'))

            숫자 += 1  # 다음 숫자로 이동
            print(컴퓨터입력)  # 컴퓨터의 입력을 출력

            플레이어차례 = True  # 플레이어의 차례로 변경

게임시작()  # 게임을 시작하는 함수인 게임시작() 함수를 호출
